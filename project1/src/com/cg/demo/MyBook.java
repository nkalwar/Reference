package com.cg.demo;

public class MyBook {
	int bookid;
	String booknam;
	public MyBook(int bookid, String booknam) {
		super();
		this.bookid = bookid;
		this.booknam = booknam;
	}
	public int getBookid() {
		return bookid;
	}
	public void setBookid(int bookid) {
		this.bookid = bookid;
	}
	public String getBooknam() {
		return booknam;
	}
	public void setBooknam(String booknam) {
		this.booknam = booknam;
	}
	
	

}
